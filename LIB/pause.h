#ifndef PAUSE_H
#define PAUSE_H

void lets_pause (){
	TTimer * timer = new TTimer("gSystem->ProcessEvents();", 50, kFALSE);
	timer->TurnOn();
	timer->Reset();
	std::cout << "q/Q to quit, other to continuee: ";
	char kkey;
	std::cin.get(kkey);
	if( kkey == 'q' || kkey == 'Q') 			gSystem->Exit(0);
	timer->TurnOff();
	delete timer;
}

#endif